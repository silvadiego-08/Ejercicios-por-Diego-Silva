# Calcular el área de un círculo
print("Calcula el área de un círculo (Recuerda que A = 3.14 * r * r)")

# Solicitar los radios al usuario
radio1 = float(input("Digite el radio1 del círculo: "))
radio2 = float(input("Digite el radio2 del círculo: "))

# Calcular el área
area = 3.14 * (radio1 * radio2)

# Mostrar el área total del círculo
print("El área total del círculo es:", area)